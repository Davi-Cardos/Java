
public class Pessoa {
	private String Cabeca;
	private String Corpo;
	private String Bracos;
	private String Pernas;
	private double QtdPessoas;
	
	public double getQtdPessoas() {
		return QtdPessoas;
	}
	public void setQtdPessoas(double qtdPessoas) {
		QtdPessoas = qtdPessoas;
	}
	public String getCabeca() {
		return Cabeca;
	}
	public void setCabeca(String cabeca) {
		Cabeca = cabeca;
	}
	public String getCorpo() {
		return Corpo;
	}
	public void setCorpo(String corpo) {
		Corpo = corpo;
	}
	public String getBracos() {
		return Bracos;
	}
	public void setBracos(String bracos) {
		Bracos = bracos;
	}
	public String getPernas() {
		return Pernas;
	}
	public void setPernas(String pernas) {
		Pernas = pernas;
	}

}

